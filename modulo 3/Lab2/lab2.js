/* Array en JavaScript */

'use strict';

var my_array = new Array();
my_array = [A,B,C,D,AA,BB,CC];

// Agregando elemento despues de la ultima posicion

my_array.push('DD');

console.log(my_array);

// Eliminar el ultimo elemento
my_array.pop();

// eliminar el primer elemento
my_array.shift();

console.log(my_array);


// Organizar elementos
my_array.sort();
console.log(my_array);

